#!/usr/bin/python3

class Circle:
    def __init__(self, radius):
        self.radius = radius
        self.color = "Blue"

class Rectangle:
    def __init__(self, width, length):
        self.width = width
        self.length = length

class Movie:
    def __init__(self, title, year, language, rating):
        self.title = title
        self.year = year
        self.language = language
        self.rating = rating

